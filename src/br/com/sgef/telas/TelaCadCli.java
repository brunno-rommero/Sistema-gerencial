    private void tblClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClienteMouseClicked
        /*Pega o id do campo codigo e faz a consulta no banco de dados,
        após fazer a consulta, pega os atributos e seta para os campos
        do formulario conforme clica sobre o registro da tabela na tela*/
        
        ClienteDAO cdao = new ClienteDAO();
        Cliente cliente = new Cliente();

        int k = tblCliente.getSelectedRow();
        Object id = tblCliente.getValueAt(k, 0);
        CodCli.setText(id.toString());
        cliente.setId(Integer.parseInt(id.toString()));
        
        //setar o restante dos campos
        int setar = tblCliente.getSelectedRow();
        NomeCli.setText(cdao.pesquisa_por_id((Integer) id).getNome());
        CidadeCli.setText(cdao.pesquisa_por_id((Integer) id).getCidade());
        ComboUf.setSelectedItem(cdao.pesquisa_por_id((Integer) id).getUf());
        TelCli.setText(cdao.pesquisa_por_id((Integer) id).getTelefone());
        EnderecoCli.setText(cdao.pesquisa_por_id((Integer) id).getEndereco());
        BairroCli.setText(cdao.pesquisa_por_id((Integer) id).getBairro());
        NumCli.setText(cdao.pesquisa_por_id((Integer) id).getNumero());
        //botao salvar inativo
        btnSave.setEnabled(false);
        

    }//GEN-LAST:event_tblClienteMouseClicked


