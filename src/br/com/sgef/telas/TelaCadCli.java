    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // Botao salvar - ao clicar valida os campos e se estiver tudo ok salva no DB
        
        Cliente cliente = new Cliente();
        ClienteDAO cdao = new ClienteDAO();
        
        if ((NomeCli.getText().isEmpty()) || (CidadeCli.getText().isEmpty()) || 
            (TelCli.getText().isEmpty()) || (EnderecoCli.getText().isEmpty()) ||
            (BairroCli.getText().isEmpty()) || (NumCli.getText().isEmpty())) {
               
            JOptionPane.showMessageDialog(null, "Preencha Todos os Campos Obrigatórios (*)");
                
        }else{
        
            cliente.setNome(NomeCli.getText());
            cliente.setCidade(CidadeCli.getText());
            cliente.setUf(ComboUf.getSelectedItem().toString());
            cliente.setTelefone(TelCli.getText());
            cliente.setEndereco(EnderecoCli.getText());
            cliente.setBairro(BairroCli.getText());
            cliente.setNumero(NumCli.getText());

            cdao.adicionar(cliente);
            
            CodCli.setText(null);
            NomeCli.setText(null);
            CidadeCli.setText(null);
            ComboUf.setSelectedIndex(1);
            TelCli.setText(null);
            EnderecoCli.setText(null);
            BairroCli.setText(null);
            NumCli.setText(null);
        }
        
        DefaultTableModel modelo = (DefaultTableModel) tblCliente.getModel();
        
        //a linha abaixo limpa a tabela antes de colocar o resultado da pesquisa
        ((DefaultTableModel) tblCliente.getModel()).setRowCount(0);
        
        for (Cliente cli: cdao.read(this.txtPesquisar.getText().toString())) {
            modelo.addRow(new Object[]{
                cli.getId(),
                cli.getNome(),
                cli.getCidade(),
                cli.getTelefone()

            });

        }
        
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // Seleciona o registro na tabela e ao clicar aqui exclui o fornecedor
        // Se nenhum registro tiver selecionado o sistema avisa o usuario
        
        Cliente cliente = new Cliente();
        ClienteDAO dao = new ClienteDAO();
        
        
        if (CodCli.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecione um registro para ser Excluido!");
        }else{
            int dialogButton = JOptionPane.YES_NO_OPTION;
            dialogButton = JOptionPane.showConfirmDialog (null, "Deseja Realmente Excluir o Cliente?",
                "ATENÇÃO", dialogButton);
            if (dialogButton == JOptionPane.YES_OPTION) {
                cliente.setId(Integer.parseInt(CodCli.getText()));
                dao.excluir(cliente);
                //limpar campos
                CodCli.setText(null);
                NomeCli.setText(null);
                CidadeCli.setText(null);
                ComboUf.setSelectedIndex(1);
                TelCli.setText(null);
                EnderecoCli.setText(null);
                BairroCli.setText(null);
                NumCli.setText(null);
                btnSave.setEnabled(true);
            
            }
            
        }
        DefaultTableModel modelo = (DefaultTableModel) tblCliente.getModel();
        
        //a linha abaixo limpa a tabela antes de colocar o resultado da pesquisa
        ((DefaultTableModel) tblCliente.getModel()).setRowCount(0);
        
        for (Cliente cli: dao.read(this.txtPesquisar.getText().toString())) {
            modelo.addRow(new Object[]{
                cli.getId(),
                cli.getNome(),
                cli.getCidade(),
                cli.getTelefone()

            });

        }

    }//GEN-LAST:event_btnExcluirActionPerformed

