    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        //Excluir usuario por id
        UserDAO user = new UserDAO();
        User u = new User();
        
        
        if(codUser.getText().toString().isEmpty()){
               JOptionPane.showMessageDialog(null, "Selecione um registro para ser Excluido!");
        }else{
            int dialogButton = JOptionPane.YES_NO_OPTION;
            dialogButton = JOptionPane.showConfirmDialog (null, "Deseja Realmente Excluir Usuário?","ATENÇÃO", dialogButton);
            if (dialogButton == JOptionPane.YES_OPTION) {

                u.setId(Integer.parseInt(codUser.getText()));
                user.excluir(u);

                //Limpar campos
                codUser.setText(null);
                nameUser.setText(null);
                loginUser.setText(null);
                pwdUser.setText(null);
                comboboxPerfilUser.setSelectedIndex(0);
                //ativa botao salvar
                btnSave.setEnabled(true);    
            }
        }
        DefaultTableModel modelo = (DefaultTableModel) tblUser.getModel();
        //a linha abaixo limpa a tabela antes de colocar o resultado da pesquisa
        ((DefaultTableModel) tblUser.getModel()).setRowCount(0);
        
        for (Iterator<User> it = user.read(this.txtPesquisar.getText()).iterator(); it.hasNext();) {
            User user1 = it.next();
            modelo.addRow(new Object[]{
                user1.getId(),
                user1.getNome(),
                user1.getUsuario(),
                user1.getSenha(),
                user1.getperilUser()
                   
            });
        }
        
    }//GEN-LAST:event_jToggleButton1ActionPerformed

