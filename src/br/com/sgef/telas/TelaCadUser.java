    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        UserDAO user = new UserDAO();
        User u = new User();// criei um objeto user 
        //marcar para o combo box ja vim marcado alguma opção
        if ((nameUser.getText().isEmpty()) || (loginUser.getText().isEmpty()) || (pwdUser.getText().isEmpty()) || (comboboxPerfilUser.getSelectedItem().toString().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Preencha Todos os Campos Obrigatórios (*)");
                
        }else{
        
            u.setNome(nameUser.getText());
            u.setUsuario(loginUser.getText());
            u.setSenha(pwdUser.getText());
            u.setperfilUser(comboboxPerfilUser.getSelectedItem().toString());

            user.adicionar(u);
            
            codUser.setText(null);
            nameUser.setText(null);
            loginUser.setText(null);
            pwdUser.setText(null);
            
        }
        
        DefaultTableModel modelo = (DefaultTableModel) tblUser.getModel();
        //a linha abaixo limpa a tabela antes de colocar o resultado da pesquisa
        ((DefaultTableModel) tblUser.getModel()).setRowCount(0);
        
        for (Iterator<User> it = user.read(this.txtPesquisar.getText()).iterator(); it.hasNext();) {
            User user1 = it.next();
            modelo.addRow(new Object[]{
                user1.getId(),
                user1.getNome(),
                user1.getUsuario(),
                user1.getSenha(),
                user1.getperilUser()
                   
            });
        }
 
    }//GEN-LAST:event_btnSaveActionPerformed

